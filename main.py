not_direction = ["encode", "decode"]
not_i = ["yes","no"]
def caesar(start_text, shift_amount, cipher_direction):
  end_text = ""
  if cipher_direction == "decode":
    shift_amount *= -1
  for char in start_text:
    if char in alphabet:
      position = alphabet.index(char)
      new_position = position + shift_amount
      end_text += alphabet[new_position]
    else:
      end_text = end_text + char
    
    
  print(color.pink + f"Here's the {cipher_direction}d result: {end_text} \n" + color.end) 
continue1 = True
while continue1:
  direction = input("Type 'encode' to encrypt, type 'decode' to decrypt:\n")
  if direction not in not_direction:
    print(f"{direction} invalid!")
    direction = input("Type 'encode' to encrypt, type 'decode' to decrypt:\n")
  text = input("Type your message:\n").lower()
  shift = int(input("Type the shift number:\n"))
  shift = shift % 26

  caesar(start_text=text, shift_amount=shift, cipher_direction=direction)
  i = input("Type 'yes' if you want to go again. Otherwise type 'no':\n").lower()
  if i not in not_i:
    print(f"{i} is invalid!")
    i = input("Type 'yes' if you want to go again. Otherwise type 'no':\n").lower()
  if i == "no":
    continue1 = False
